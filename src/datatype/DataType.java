package datatype;

/*#Data Type - The base of any programming language is its data types.

1.Primitive Type.
2.Non-Primitive Type.

# Primitive Type - The programming language pre-defines the primitive data types.
Primitive types are the most basic data types available in Java.

# Non-Primitive Type - Non-primitive data types are not defined or created by
  Java.

*/

public class DataType {

	public static void main(String[] args) {
		
		//primitive data type

		char Char1 = 'A';
	    byte Byte1 = 127;
	    short Short1 = 6000;
		int Num1 = -7000;
		long Long1 = 100000000L;
		float Float1=1000.666f;
		double Double1 = 48976.8987;
	    boolean Bool = true;

	    
	    System.out.println(Char1);
	    System.out.println(Byte1);
	    System.out.println(Short1);
	    System.out.println(Num1);
	    System.out.println(Long1);
	    System.out.println(Float1);
	    System.out.println(Double1);
	    System.out.println(Bool);

	}

}
